def MiniStringFuck_Interpreter(code)
  @count = 0
  @res = ''
  code.scan(/[+|.]+/).join.each_char do |c| 
    if c == '+'
      @count+=1
    else
      @res += @count.%(256).chr
    end
  end
  @res
end





